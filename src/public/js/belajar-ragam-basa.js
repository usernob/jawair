window.onload = function() {
    const tb = document.getElementById("list-aksara")
    let tr = []
    list.forEach(i => {
        tr.push('<tr><td class="text-xs md:text-base">'+i.indonesia+'</td>')
        tr.push('<td class="text-xs md:text-base">'+i.ngoko_lugu+'</td>')
        tr.push('<td class="text-xs md:text-base">'+i.ngoko_alus+'</td>')
        tr.push('<td class="text-xs md:text-base">'+i.krama_lugu+'</td>')
        tr.push('<td class="text-xs md:text-base">'+i.krama_alus+'</td></tr>')
    })
    tb.innerHTML = tr.join("")
}

// head list basa
var prevList = window.pageYOffset
window.onscroll = function () {
    var scrol = window.pageYOffset
    if (prevList > scrol) {
        document.getElementById('head-list-basa').classList.remove('show')
    } else {
        document.getElementById('head-list-basa').classList.add('show')
    }
    prevList = scrol
}
